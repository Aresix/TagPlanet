package com.aresix.tagplanet.entity;

public class Item {
    private int rsc_id;
    private String rsc_url;
    private String rsc_type;
    private String rsc_desc;

    public int getRsc_id() {
        return rsc_id;
    }

    public void setRsc_id(int rsc_id) {
        this.rsc_id = rsc_id;
    }

    public String getRsc_url() {
        return rsc_url;
    }

    public void setRsc_url(String rsc_url) {
        this.rsc_url = rsc_url;
    }

    public String getRsc_type() {
        return rsc_type;
    }

    public void setRsc_type(String rsc_type) {
        this.rsc_type = rsc_type;
    }

    public String getRsc_desc() {
        return rsc_desc;
    }

    public void setRsc_desc(String rsc_desc) {
        this.rsc_desc = rsc_desc;
    }
}
